    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String tipoId = selectIdentidad3.getSelectedItem().toString();
        String numero = telefonoPsico.getText();
        
        if (tipoId.isEmpty()||tipoId.equals("Seleccionar")||numero.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar su tipo de identificación e ingresar su nuevo número de teléfono", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(numero.matches("\\d+")){
                
                try{
                    
                    UsuarioActivo idUsuario = UsuarioActivo.getInstance();
                    
                    PreparedStatement psConsultaId = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.SELECT_IDPACIENTE_IDUSUARIO, String.valueOf(idUsuario.getIdUsuario())));
                    ResultSet rsConsultaId = psConsultaId.executeQuery();
                    rsConsultaId.next();

                    String idPaciente = rsConsultaId.getString("ID_PACIENTE");
                                        
                    PreparedStatement psPaci = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.UPDATE_PACIENTE_IDPACIENTE, tipoId, numero, idPaciente));
                    psPaci.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null, "Se actualizó su perfil con éxito", "Éxito",JOptionPane.INFORMATION_MESSAGE);
                    
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                }
                
            }else{
                JOptionPane.showMessageDialog(null, "Solo puede ingresar números en el campo teléfono", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }

    }//GEN-LAST:event_jButton4ActionPerformed

