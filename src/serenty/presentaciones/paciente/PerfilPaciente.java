    private void datos(){        
        
        try {
            
            UsuarioActivo idUsuario = UsuarioActivo.getInstance();
            
            PreparedStatement psConsultaId = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.SELECT_IDPACIENTE_IDUSUARIO, String.valueOf(idUsuario.getIdUsuario())));
            ResultSet rsConsultaId = psConsultaId.executeQuery();
            rsConsultaId.next();

            String idPaciente = rsConsultaId.getString("ID_PACIENTE");
                        
            PreparedStatement psConsultaPaciente = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.SELECT_PACIENTE_IDPACIENTE, idPaciente));
            ResultSet rsConsultaPaciente = psConsultaPaciente.executeQuery();
            
            if (rsConsultaPaciente.next()) {
                
                String nombrePaciente = rsConsultaPaciente.getString("NOMBRE_PACIENTE");
                String apellidoPaciente = rsConsultaPaciente.getString("APELLIDO_PACIENTE");               
                
                numId.setText(idPaciente);
                nombre.setText(nombrePaciente);
                apellido.setText(apellidoPaciente);
            }
           
            psConsultaPaciente.close();
            rsConsultaPaciente.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

