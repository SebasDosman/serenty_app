    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String modali = modalidad.getSelectedItem().toString();
        String horaCita = hora.getSelectedItem().toString();
        String direc = direccion.getText();
        Date fechaCita = fechaReagendada.getDate();
        String descr = descripcion.getText();
        String estadoCita = "Reagendada";
        
        SimpleDateFormat simpleDateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String fecha_formateada = simpleDateFormat.format(fechaCita);

        Calendar calendarManana = Calendar.getInstance();
        calendarManana.add(Calendar.DAY_OF_YEAR, 1);
        calendarManana.set(Calendar.HOUR_OF_DAY, 0);
        calendarManana.set(Calendar.MINUTE, 0);
        calendarManana.set(Calendar.SECOND, 0);
        calendarManana.set(Calendar.MILLISECOND, 0);
        Date manana = calendarManana.getTime();
        
        if (modali.isEmpty()||modali.equals("Seleccionar")||horaCita.isEmpty()||horaCita.equals("Sleccionar")||direc.isEmpty()||fechaCita == null||descr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if (fechaCita.before(manana)){
                JOptionPane.showMessageDialog(null, "Debes agendar la cita a partir de mañana", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                try{
                    
                    int selectedRow = jTable1.getSelectedRow();
                    Object idCita = jTable1.getValueAt(selectedRow, 0);
                                        
                    PreparedStatement psCita = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.UPDATE_CITA_IDCITA_DOS, fecha_formateada, modali, horaCita, direc, descr, estadoCita, idCita));
                    psCita.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null, "Se reagendó la cita con éxito", "Éxito",JOptionPane.INFORMATION_MESSAGE);
                    
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Debe seleccionar un psicólogo de la tabla", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


