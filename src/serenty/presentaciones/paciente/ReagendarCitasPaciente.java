    public void MostrarCitas() throws SQLException {
        
        Object[] row = new Object[8];
        
        model.addColumn("ID_CITA");
        model.addColumn("NOMBRE");
        model.addColumn("APELLIDO");
        model.addColumn("FECHA_CITA");
        model.addColumn("HORA_CITA");
        model.addColumn("MODALIDAD_CITA");
        model.addColumn("DIRECCIÓN_CITA");
        model.addColumn("ESTADO_CITA");
        
        jTable1.setModel(model);
        
        try {
            
            UsuarioActivo idUsuario = UsuarioActivo.getInstance();
            
            PreparedStatement psConsultaPaci = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.SELECT_IDPACIENTE_IDUSUARIO, String.valueOf(idUsuario.getIdUsuario())));
            ResultSet rsConsultaPaci = psConsultaPaci.executeQuery();
            rsConsultaPaci.next();
            
            String idPaciente = rsConsultaPaci.getString("ID_PACIENTE");            
            
            PreparedStatement ps = (PreparedStatement) cn.prepareStatement(String.format(ConsultasDB.SELECT_CITA_IDPACIENTE, idPaciente));
            ResultSet rs = ps.executeQuery();
            
            while (rs.next()) {
                row[0] = rs.getObject("ID_CITA");
                row[1] = rs.getObject("NOMBRE_PSICOLOGO");
                row[2] = rs.getObject("APELLIDO_PSICOLOGO");
                row[3] = rs.getObject("FECHA_CITA");
                row[4] = rs.getObject("HORA_CITA");
                row[5] = rs.getObject("MODALIDAD_CITA");
                row[6] = rs.getObject("DIRECCION_CITA");
                row[7] = rs.getObject("ESTADO_CITA");
                
                model.addRow(row);
            }
            
            jTable1.setModel(model);
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

